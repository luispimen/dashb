package com.jcg.examples.entity;

public class SignosVitales {
	private int costo;
	private int avance;
	private int documento;
	private int rrhh;
	private int problemas;
	private int controlDeCambio;

	public int getCosto() {
		return costo;
	}

	public void setCosto(int costo) {
		this.costo = costo;
	}

	public int getAvance() {
		return avance;
	}

	public void setAvance(int avance) {
		this.avance = avance;
	}

	public int getDocumento() {
		return documento;
	}

	public void setDocumento(int documento) {
		this.documento = documento;
	}

	public int getRrhh() {
		return rrhh;
	}

	public void setRrhh(int rrhh) {
		this.rrhh = rrhh;
	}

	public int getProblemas() {
		return problemas;
	}

	public void setProblemas(int problemas) {
		this.problemas = problemas;
	}

	public int getControlDeCambio() {
		return controlDeCambio;
	}

	public void setControlDeCambio(int controlDeCambio) {
		this.controlDeCambio = controlDeCambio;
	}

}
