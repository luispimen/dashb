package com.jcg.examples.entity;

public class Plan {
	private String fecha;
	private long plan;
	private long real;

	public String getFecha() {
		return fecha;
	}

	public void setFecha(String fecha) {
		this.fecha = fecha;
	}

	public long getPlan() {
		return plan;
	}

	public void setPlan(long plan) {
		this.plan = plan;
	}

	public long getReal() {
		return real;
	}

	public void setReal(long real) {
		this.real = real;
	}

}
